# 기본 연산자
print(1+1) # 2
print(3-2) # 1
print(5*2) # 10
print(6/3) # 2 나누기의 경우 -> float 

# 제곱과 나머지 , 몫
print(2**3) # 2^3
print(2%3) # 나머지
print(2//3) # 몫 구하기 

# 연산자
print(10>3) #True
print(4 >= 7) #False
print(10<3) #/False
print(5<=5) #True

# 등호 연산자
print(3==3) #True
print(3!=3) #False
print(not (3!=3))

# and &
print((3>0)and(3>5)) #True
print((3>0) & (3<5)) # True 

#or |
print((3>0)or(3<5)) # True
print((3>0) | (3<5)) # True


